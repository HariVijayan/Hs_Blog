         <p id="articleinfo">From now on, Uttar Pradesh farmers will not have to go to the purchase centre to sell wheat in the state. The Food and Civil Supplies Department is making arrangements for farmers to purchase wheat from village to village through mobile centres.<br><br>For this, a purchasing centre in charge will negotiate with the fair rate vendors and village heads of the rural areas. Wheat used to be procured at government centres across the state. However, about 5,700 mobile procurement centres have been set up now for the same. Drinking water, shade and other necessary arrangements have been made at the centres so that the farmers do not face any kind of problem.<br><br>Only 1.28 lakh metric tonnes of wheat could be procured in the last month, while the government had set a target of purchasing 60 lakh tonnes. The government has been facing major challenges to fulfil the target of wheat procurement.<br><br>On the other hand, farmers sell wheat for Rs 2050-Rs 2200 per quintal in the market in Uttar Pradesh, whereas the MSP at government procurement centres is Rs 2015 per quintal, including Rs 20 for labour cost. Therefore, to increase procurement, it has been decided to buy wheat through mobile centres.
