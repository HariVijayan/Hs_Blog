         <p id="articleinfo">On Thursday, a day after he took over as the LDF convener, E P Jayarajan lavished praise on senior IUML leader P K Kunhalikutty, dubbing him the kingmaker of political tactics.The ruling CPM’s open invitation to Congress ally Indian Union Muslim League (IUML) to join the Left Democratic Front (LDF), received by an ambiguous response by the latter, has created a flutter in Kerala.<br><br>On Thursday, a day after he took over as the LDF convener, E P Jayarajan lavished praise on senior IUML leader P K Kunhalikutty, dubbing him the kingmaker of political tactics. Saying expansion of the LDF was the CPM’s agenda, Jayarajan said: “The doors of the LDF are open. If the IUML leaves the Congress-led UDF, we can think about its entry into the LDF.”<br><br>Jayarajan also claimed that “resentment was brewing” within the IUML over continuation of ties with the Congress-led UDF. And that “the Congress is a worried lot as it cannot exist without the IUML”.<br><br>Kunhalikutty only fanned speculations further when, instead of a denial, he reacted with: “Leaving the UDF is not on the agenda of the IUML at present. The League is now firmly with the UDF.”<br><br>With the LDF in on-again, off-again flirtation with the IUML for long, CPI state secretary Kanam Rajendran raised objections to Jayarajan’s statement. However, the junior Communist party has but a nominal role to play in the decision-making process of the LDF, where only the CPM calls the shots.The CPM has targeted the IUML as a “communal” outfit in the past, and dubbed it part of “the Congress-BJP communal alliance’’ against the Left. Even at the recently held state conference of the party, the CPM had accused the IUML of being soft on extremist elements.While the LDF in Kerala with 99 seats is comfortably placed in the 140-strong Assembly, expansion by splitting the UDF remains one of its top goals. The Congress is its only real rival in the state still, and the CPM talks publicly of aiming for power beyond the current term, which ends in 2026.Jayarajan’s overture to the IUML follows the CPM earlier establishing a direct line to the Samastha Kerala Jemiyyathul Ulema, popularly known as Samastha, a powerful body of pro-IUML Muslim scholars. Recently, when the IUML announced a massive agitation against the Pinarayi Vijayan government over its move to hand over Wakf Board recruitment to the Kerala Public Service Commission, Samastha stayed out of the stir at Vijayan’s behest. This was a blow to the IUML’s confidence regarding its control over the Samastha.
