         <p id="articleinfo">Thalapathy Vijay’s Beast was released on April 13 and received mixed reviews from both, the audience and the critics. However, there is one person who is disappointed with the movie’s screenplay. Well, it’s none other than Vijay’s father and filmmaker SA Chandrasekhar.During a recent interview, SA Chandrasekhar expressed disappointment with the movie and slammed the makers for solely relying on Vijay’s stardom. He went on to say that young filmmakers do not concentrate on the screenplay once they get big actors on board.<br><br><br>“I enjoyed Arabic Kuthu like how a die-hard fan would. But, this is a film that was made solely relying on Vijay’s stardom. Young filmmakers deliver an excellent first film in terms of content, technology and making. They also somehow achieve good results with the second film. When they begin to get the projects of big superstars, these young filmmakers begin to think, ‘now that we have dates of these heroes, we can make movies whichever way we want.’ They think he (the star) already has a huge fan following and his film will run successfully in theatres, so no need for a screenplay and things like that. And they make a movie with a few songs and fights,” Chandrasekhar told Thanthi TV recently as cited by IndianExpress.com.
