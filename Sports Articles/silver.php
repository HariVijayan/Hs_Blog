         <p id="articleinfo">All of them are in their early-to-mid 60s and hail from varied backgrounds — stockbroker to industrialist, banker to publisher.For Anil Padhye, it was the late evening deals with his maternal grandparents in the 1980s that got him hooked to bridge. His teammate, Ashok Goel, stayed up late at night to master the skills of the card game. And their captain, R Krishnan, took to the sport casually at first, in his hostel room at IIT Madras, exactly 50 years ago in 1972.<br><br>The trio was part of a six-member team that won India’s first-ever silver medal at the World Bridge Championship earlier this month. All of them are in their early-to-mid 60s and hail from varied backgrounds — stockbroker to industrialist, banker to publisher.<br><br>Subrata Saha, Sukamal Das and Rajesh Dalal were the other members of the team that defeated heavyweights like the US en route to the final of the seniors category, where they lost to a strong Polish side.“It’s an important moment for the sport in the country,” Padhye said. “For the first time, an Indian team has reached the final of the World Championship. This augurs well for the sport and hopefully will change the perception among the masses.”<br><br>Popularly known as the ‘Bermuda Bowl’, the World Championship in the Italian city of Salsomaggiore attracted 24 teams from across the globe. After the round-robin stage, India finished fifth, qualifying for the knockout rounds where the top-eight teams made the cut.“The team that finishes on top after the round-robin stage gets to pick the team from the top-eight they want to play in the quarterfinals,” explained Krishnan. “In this case, the US, which had fielded two teams, was first and chose us, assuming we’ll be easy to beat.”<br><br>After hours of heavy dealing, which saw both teams taking turns to get into the lead, India recorded an upset and progressed to the semi-final, where they defeated France. That ensured a historic finish, given that no Indian team had ever reached that far in a World Championship.
