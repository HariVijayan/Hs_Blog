         <p id="articleinfo">Bengaluru: Wipro said today that it will acquire SAP consulting firm Rizing Intermediate Holdings Inc for $540 million in an all cash deal, marking the Bengaluru-based company's second acquisition this month. The transaction is expected to be completed before quarter ending June 30 subject to requisite regulatory approvals and customary closing conditions, according to the exchange filing.Rizing’s industry expertise and SAP consulting capabilities in enterprise asset management, consumer industries, and human experience management, will be "instrumental" in advancing Wipro’s position as an advisor for clients’ most complex SAP transformations, it said in a statement.<br><br><br>The Connecticut, US based company has over 1,300 employees and over 20 offices across North America, Europe, Asia and Australia. It reported a consolidated net revenue of $193.8 million for the year ended December 2021 and $138.3 million in 2020.Wipro, in its investor presentation, identified strengthening Wipro SAP focus industries of retail & consumer goods, energy & utilities as strategic rationale for the acquisition. The transaction will also bring deep consulting capabilities and access to Fortune 2000 companies, it added.
