         <p id="articleinfo">Instagram will soon be downranking Reels videos that are not original to the platform. This means that all reshared content from other apps like TikTok or any short video-format platform will be downranked to give more credit to original content.<br><br>Instagram head Adam Mosseri recently took to Twitter to announce the new changes to the app. In a video, Mosseri explains that Instagram will be adding three new features/changes to the platform – Product tags, enhanced people tags and higher ranking for original Reels.<br><br>“If you create something from scratch, you should get more credit than if you are resharing something that you found from someone else,” Mosseri said in the video. This change leaves a few things up in the air.<br><br>But what happens to Reels that are reshared videos from your own handles on other platforms? Are they any less original than videos you create on Instagram Reels directly? We should find out soon enough.<br><br>Responding to a comment when providing some more clarity on the matter, Mosseri said that “identifying originality is hard” and that the platform will “iterate over time” on what counts as original and what does not. Until there is more clarity on the same, the move could also be interpreted as a tactic to further persuade people from not resharing videos from platforms like TikTok on Reels.<br><br>While Reels has done well in markets like India, one reason is that TikTok is banned here. But in markets like the US, UK, etc, Instagram Reels faces tough competition from TikTok, which remains the leading short video platform. This would explain why Instagram is insisting creators make original content for Reels, rather than just reposting their TikTok video to the platform. After all, it wants less videos that have the TikTok logo added to them, which is currently the case for a lot of the viral content on the platform.
